CREATE TABLE Enseigne
(
	CodeModule VARCHAR(10) PRIMARY KEY NOT NULL,
	IdEnseignant Int PRIMARY KEY NOT NULL,
  FOREIGN KEY(CodeModule) REFERENCES Module(Code),
  FOREIGN KEY(IdEnseignant) REFERENCES Enseignant(num_ens)
